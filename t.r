# Degrees of freedom
df = 9  # ডিগ্রিস অব ফ্রিডম ৯

# (a) ডান পাশে শেড করা ক্ষেত্রের অংশ = 0.05
t1_a = qt(0.95, df)  # cumulative অংশ বাম দিকে = 0.95

# (b) মোট শেড করা অংশ = 0.05 (দুই দিকে, মানে প্রতি পাশে 0.025)
t1_b_upper = qt(0.975, df)  # উপরের ক্রিটিকাল মান
t1_b_lower = qt(0.025, df)  # নিচের ক্রিটিকাল মান

# (c) মোট অশেড করা অংশ = 0.99 (শেড করা অংশ = 0.01, দুই দিকে, মানে প্রতি পাশে 0.005)
t1_c_upper = qt(0.995, df)  # উপরের ক্রিটিকাল মান
t1_c_lower = qt(0.005, df)  # নিচের ক্রিটিকাল মান

# (d) বাম পাশে শেড করা অংশ = 0.01
t1_d = qt(0.01, df)  # cumulative অংশ বাম দিকে = 0.01

# (e) বাম দিকে কিউমুলেটিভ এরিয়া = 0.90
t1_e = qt(0.90, df)  # cumulative অংশ বাম দিকে = 0.90

# ফলাফল দেখানো
cat("(a) t1 =", t1_a, "\n")  # (a) এর ফলাফল
cat("(b) Upper =", t1_b_upper, "Lower =", t1_b_lower, "\n")  # (b) এর ফলাফল
cat("(c) Upper =", t1_c_upper, "Lower =", t1_c_lower, "\n")  # (c) এর ফলাফল
cat("(d) t1 =", t1_d, "\n")  # (d) এর ফলাফল
cat("(e) t1 =", t1_e, "\n")  # (e) এর ফলাফল
